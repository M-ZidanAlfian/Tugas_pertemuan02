package Soal3;

public class Main {
    public static void main(String[] args) {
        // kode ASCII dari karakter '4' dan '6'
        char karakter4 = '4';
        char karakter6 = '6';

        int kodeASCII4 = (int) karakter4;
        int kodeASCII6 = (int) karakter6;
        // Menjumlahkan kode ASCII
        int hasilJumlah = kodeASCII4 + kodeASCII6;
        // Menampilkan hasil
        System.out.println("Kode ASCII dari '4': " + kodeASCII4);
        System.out.println("Kode ASCII dari '6': " + kodeASCII6);
        System.out.println("Hasil penjumlahan kode ASCII : " + hasilJumlah);
    }
}
    
    

